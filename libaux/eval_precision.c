#include "libaux.h"
#include "../libft/libft.h"

int	eval_precision(t_chunk *chunk, char *str)
{
	int	i;

	i = 0;
	if (str[i] == '.')
	{
		i++;
		if (str[i] == '*')
		{
			chunk->precision = ft_strdup("*");
			return (i + 1);
		}
		while (ft_isdigit(str[i]) != 0)
		{
			i++;
		}
		if (i > 1)
			chunk->precision = ft_substr(str, 0, i);
		else
			chunk->precision = ft_strdup("0");
	}
	return (i);
}
